 async function retrocess_get_value(log) {

	if (log == "0") {
		return "A";
	} else if (log == "1") {
		return "B";
	} else if (log == "2") {
		return "C";
	} else if (log == "3") {
		return "D";
	} else if (log == "4") {
		return "E";
	} else if (log == "5") {
		return "F";
	} else if (log == "6") {
		return "G";
	} else if (log == "7") {
		return "H";
	} else {
		return "A0";
	}

}


export default retrocess_get_value;